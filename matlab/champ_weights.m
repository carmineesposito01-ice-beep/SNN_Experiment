function W = champ_weights(c)
%CHAMP_WEIGHTS  Struct pesi standard di un champion (scalari in double), per core / MEX.
%  Condiviso da build_traj_mex e run_b15a_validate: garantisce tipi coerenti fra la
%  generazione del MEX e la sua chiamata a runtime.
  W = struct('hidden', double(c.hidden), 'rank', double(c.rank), 'n_ticks', double(c.n_ticks), ...
    'max_delay', double(c.max_delay), 'fc_weight', c.fc_weight, 'rec_U', c.rec_U, ...
    'rec_V', c.rec_V, 'readout', c.readout, 'delays', c.delays, ...
    'base_threshold', c.base_threshold, 'thresh_jump', c.thresh_jump, ...
    'leak_div', c.leak_div, 'param_lo', c.param_lo, 'param_hi', c.param_hi, ...
    'decode_offset', c.decode_offset, 'logit_tau', c.logit_tau, 'norm', c.norm);
end
