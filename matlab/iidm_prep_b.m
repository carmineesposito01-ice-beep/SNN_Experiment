function st = iidm_prep_b(s, v, dv, v_l, p, alf, sabin) %#codegen
%IIDM_PREP_B  [R8] Cast, guardie e costruzione dello stato `st`, a partire dall'`alf` GIA' aggiornato
%  da iidm_prep_a (registrato fra i due clock). Espressioni VERBATIM da iidm_prep.
  T = acc_types('fixed');
  v0 = max(p(1), 1e-3); T_ = max(p(2), 1e-3); s0 = p(3);   % a,b -> iidm_ab (servono anche a iidm_sabx)

  sq = cast(s,  'like', T.st);   vq = cast(v,   'like', T.st);
  dq = cast(dv, 'like', T.st);   lq = cast(v_l, 'like', T.st);   %#ok<NASGU>
  v0f = cast(v0, 'like', T.par); Tf_ = cast(T_, 'like', T.par); s0f = cast(s0, 'like', T.par);
  [af, bf] = iidm_ab(p);         % fonte unica, condivisa con iidm_sabx (l'ingresso della radice)

  sab    = cast(max(sabin, 1e-6), 'like', T.par);   % la radice arriva GIA' FATTA
  s_safe = cast(max(sq, 2.0), 'like', T.st);

  % Tipi PIENI dei quadrati (R5/R7): stringerli cambierebbe la matematica (bug §2.1).
  sq0 = cast(0, 'like', T.acc) * cast(0, 'like', T.acc);
  sq1 = sq0 * sq0;
  sq2 = 1 - sq0;
  vt0 = cast(0, 'like', T.st) * cast(0, 'like', T.par);   % [R15] tipo pieno di vq*Tf_     % [R13] tipo pieno di (1 - z^2): isolare la sottrazione dai prodotti

  st = struct( ...
    'vq',      vq, ...
    'dq',      dq, ...
    'v0f',     v0f, ...
    'Tf_',     Tf_, ...
    's0f',     s0f, ...
    'af',      af, ...
    'bf',      bf, ...
    'sab',     sab, ...
    's_safe',  s_safe, ...
    'a_l_bar', cast(min(alf, af), 'like', T.acc), ...
    's_star',  cast(0, 'like', T.st), ...
    'v_free',  cast(0, 'like', T.acc), ...
    'z',       cast(0, 'like', T.acc), ...
    'a_iidm',  cast(0, 'like', T.acc), ...
    'a_cah',   cast(0, 'like', T.acc), ...
    'dd',      cast(0, 'like', T.acc), ...
    'uu',      cast(0, 'like', sq0), ...
    'zz',      cast(0, 'like', sq0), ...
    'uu2',     cast(0, 'like', sq1), ...
    'w',       cast(0, 'like', sq2), ...
    'vt',      cast(0, 'like', vt0), ...
    'qm',      cast(0, 'like', T.acc));
end
